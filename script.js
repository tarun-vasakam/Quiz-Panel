function red1(){
    let other=document.querySelectorAll(".opt-ans")
    for (let i=0;i<other.length;i++)
    {
        other[i].style.backgroundColor="white"
    }
    let change=document.querySelector(".red1")
    change.style.backgroundColor="crimson"
}
